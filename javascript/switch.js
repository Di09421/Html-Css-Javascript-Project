let day = Number(prompt("Enter the day value?"));

switch(day)
{
    case 1:
        document.write("Monday");
        break;
        case 2:
            document.write("Tuesday");
            break;
            case 3:
            document.write("Wednesday");
            break;
            case 4:
            document.write("Thursday");
            break;
            case 5:
            document.write("Friday");
            break;
            case 6:
            document.write("Saturday");
            break;
            case 7:
            document.write("Sunday");
            break;
            default:
                document.write("invalid input");
}

var marks = Number(prompt("Enter the marks?"));

switch(true)
{
    case marks >=33 && marks<60 :
        document.write("C");
        break;
        case marks >=60 && marks<80 :
            document.write("B");
            break;
            case marks >=80 && marks<90 :
                document.write("A");
        break;
        case marks >=90 && marks<98 :
            document.write("A+");
        break;   
        default:
            document.write("invalid value");
}

